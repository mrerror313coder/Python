# if statement
number = int(input("Enter a number: "))
if number % 2 == 0:
    print(number,"is Even Number")

# if else statement
number = int(input("Enter a number: "))
if number % 2 == 0:
    print(number,"is Even Number")
else:
    print(number,"is Odd Number")

